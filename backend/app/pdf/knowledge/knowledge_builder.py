class FinancialKnowledgeBuilder:

    """
    Enterprise Financial Knowledge Builder
    """

    @staticmethod
    def build(

        company,

        metrics,

        risks,

        opportunities,

        strategies,

        entities,

        outlook,

        sections,

    ):

        return {

            "company":

                company,

            "metrics":

                metrics,

            "risks":

                risks,

            "opportunities":

                opportunities,

            "strategies":

                strategies,

            "entities":

                entities,

            "outlook":

                outlook,

            "sections":

                sections,

        }